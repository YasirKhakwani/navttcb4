<?php
// For Loop
echo "For Loop: ";
for ($i = 5; $i <= 15; $i++) {
    echo $i . " ";
}
echo "\n";

// While Loop
echo "While Loop: ";
$j = 5;
while ($j <= 15) {
    echo $j . " ";
    $j++;
}
echo "\n";

// Do...while Loop
echo "Do...while Loop: ";
$k = 5;
do {
    echo $k . " ";
    $k++;
} while ($k <= 15);
?>
